import type { NextPage } from 'next'
import Head from 'next/head'
import FocusGraph from '../components/Graphs/GraphWrapper'
import Header from '../components/Header/Header'
import Searchbox from '../components/Searchbox/Searchbox'
import SearchResults from '../components/SearchResults/SearchResults'
import { useState } from 'react'
//import styles from '../styles/Home.module.css'


const Home: NextPage = () => {

  const[ comp, setComp ] = useState(0)
  const[ searchData, setSearchData ] = useState({})

  const setComponent = (comp: number, dataObj: any) => {
    setComp(comp)
    setSearchData(dataObj)
    console.log(searchData)
    console.log(comp)
  }
  return (
    <div className='bg-black'>
      <Head>
        <title>Ordverse</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>
        <Header />
        <Searchbox getCompData={setComponent}/>
        {comp === 0 ? <FocusGraph /> : <SearchResults data={searchData}/>}
      </main>
    </div>

  )
}

export default Home
